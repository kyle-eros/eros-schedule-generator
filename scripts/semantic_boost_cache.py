def main():
    """CLI entry point for cache management."""
    import argparse

    parser = argparse.ArgumentParser(
        description="Manage semantic boost cache files.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
    # Check if cache exists
    python semantic_boost_cache.py --creator grace_bennett --week 2025-W50 --check

    # Show cache stats
    python semantic_boost_cache.py --creator grace_bennett --week 2025-W50 --stats

    # List all cached weeks for a creator
    python semantic_boost_cache.py --creator grace_bennett --list

    # Load and display cache contents
    python semantic_boost_cache.py --creator grace_bennett --week 2025-W50 --show
        """,
    )

    parser.add_argument("--creator", "-c", required=True, help="Creator page name")
    parser.add_argument("--week", "-w", help="Week in ISO format (YYYY-Www)")
    parser.add_argument("--check", action="store_true", help="Check if cache exists")
    parser.add_argument("--stats", action="store_true", help="Show cache statistics")
    parser.add_argument("--list", action="store_true", help="List all cached weeks")
    parser.add_argument("--show", action="store_true", help="Display cache contents")
    parser.add_argument("--clear", action="store_true", help="Delete cache file")
    parser.add_argument("--cache-dir", help="Override cache directory")

    args = parser.parse_args()

    cache = SemanticBoostCache(args.cache_dir)

    if args.list:
        weeks = cache.list_cached_weeks(args.creator)
        if weeks:
            print(f"Cached weeks for {args.creator}:")
            for week in weeks:
                print(f"  - {week}")
        else:
            print(f"No cached semantic analysis for {args.creator}")
        return

    if not args.week:
        parser.error("--week is required for this operation")

    if args.check:
        exists = cache.exists(args.creator, args.week)
        if exists:
            print(f"Cache exists: {cache.get_cache_path(args.creator, args.week)}")
        else:
            print("Cache does not exist")
        return

    if args.stats:
        stats = cache.get_cache_stats(args.creator, args.week)
        if stats["exists"]:
            print(f"Semantic Cache Statistics for {args.creator} / {args.week}")
            print("-" * 50)
            print(f"  Path: {stats['path']}")
            print(f"  Generated: {stats['generated_at']}")
            print(f"  Results: {stats['result_count']}")
            print(f"  Avg Boost: {stats['avg_boost']:.2f}x")
            print(f"  Min Boost: {stats['min_boost']:.2f}x")
            print(f"  Max Boost: {stats['max_boost']:.2f}x")
            print(f"  Boost Coverage: {stats['boost_coverage']:.1f}%")
        else:
            print("Cache does not exist")
        return

    if args.show:
        boosts = cache.load(args.creator, args.week)
        if boosts:
            print(f"Semantic Boost Results for {args.creator} / {args.week}")
            print("-" * 70)
            print(f"{'Caption ID':<12} {'Tone':<12} {'Boost':<8} {'Confidence':<12} Reasoning")
            print("-" * 70)
            for caption_id, result in sorted(boosts.items()):
                reasoning = result.reasoning[:40] + "..." if len(result.reasoning) > 40 else result.reasoning
                print(
                    f"{caption_id:<12} {result.detected_tone:<12} "
                    f"{result.persona_boost:.2f}x   {result.tone_confidence:.2f}        {reasoning}"
                )
        else:
            print("No cached results found")
        return

    if args.clear:
        if cache.clear(args.creator, args.week):
            print(f"Deleted cache for {args.creator} / {args.week}")
        else:
            print("Cache does not exist")
        return

    parser.print_help()


if __name__ == "__main__":
    main()
